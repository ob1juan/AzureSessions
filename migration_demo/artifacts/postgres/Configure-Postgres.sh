CREATE OR REPLACE FUNCTION saleslt.recalculate_order_totals()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
    changed_order_id integer;
    new_subtotal numeric(19,4);
BEGIN
    changed_order_id := COALESCE(NEW.sales_order_id, OLD.sales_order_id);
    SELECT COALESCE(SUM(line_total), 0) INTO new_subtotal
    FROM saleslt.sales_order_detail
    WHERE sales_order_id = changed_order_id;

    UPDATE saleslt.sales_order_header
    SET subtotal = new_subtotal,
        tax_amt = round(new_subtotal * 0.0825, 4),
        freight = CASE WHEN new_subtotal = 0 THEN 0 ELSE round(12.50 + (new_subtotal * 0.015), 4) END,
        modified_date = now()
    WHERE sales_order_id = changed_order_id;

    RETURN COALESCE(NEW, OLD);
END;
$$;

CREATE OR REPLACE FUNCTION saleslt.set_sales_order_number()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    IF NEW.sales_order_number IS NULL THEN
        NEW.sales_order_number := 'SO' || NEW.sales_order_id::text;
    END IF;
    RETURN NEW;
END;
$$;

