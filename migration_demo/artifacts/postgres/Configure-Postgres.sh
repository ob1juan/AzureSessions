    END IF;
END
\$\$;
SELECT 'CREATE DATABASE ${WEB_DB} OWNER ${WEB_USER}'
WHERE NOT EXISTS (SELECT 1 FROM pg_database WHERE datname = '${WEB_DB}')\gexec
