wait_for_apt_locks() {
    while sudo fuser /var/lib/dpkg/lock >/dev/null 2>&1 || \
          sudo fuser /var/lib/dpkg/lock-frontend >/dev/null 2>&1 || \
          sudo fuser /var/lib/apt/lists/lock >/dev/null 2>&1 || \
          sudo fuser /var/cache/apt/archives/lock >/dev/null 2>&1; do
        echo 'Waiting for apt/dpkg locks to clear'
        sleep 10
    done
}

run_apt_with_retry() {
    local attempt=1
    local max_attempts=5

    while (( attempt <= max_attempts )); do
        wait_for_apt_locks
        sudo dpkg --configure -a >/dev/null 2>&1 || true
        if sudo apt-get -o DPkg::Lock::Timeout=300 "$@"; then
            return 0
        fi

        echo "apt-get $* failed on attempt ${attempt}/${max_attempts}" >&2
        sudo apt-get install -f -y >/dev/null 2>&1 || true
        ((attempt++))
        sleep 15
    done

    echo "apt-get $* failed after ${max_attempts} attempts" >&2
    return 1
}

echo 'Updating apt and installing PostgreSQL, Apache, Java, and Tomcat'
run_apt_with_retry update
run_apt_with_retry install -y postgresql postgresql-contrib apache2

if apt-cache show tomcat10 >/dev/null 2>&1; then
    TOMCAT_PACKAGE='tomcat10'
else
    TOMCAT_PACKAGE='tomcat9'
fi
TOMCAT_SERVICE="${TOMCAT_PACKAGE}"
TOMCAT_BASE="/var/lib/${TOMCAT_PACKAGE}"

run_apt_with_retry install -y default-jdk "${TOMCAT_PACKAGE}" libpostgresql-jdbc-java curl

PG_VERSION=$(ls /etc/postgresql 2>/dev/null | sort -V | tail -n 1)
PG_CONF="/etc/postgresql/${PG_VERSION}/main/postgresql.conf"
PG_HBA="/etc/postgresql/${PG_VERSION}/main/pg_hba.conf"

echo "Configuring PostgreSQL ${PG_VERSION} to listen on 0.0.0.0"
sudo sed -i "s/^#\?listen_addresses.*/listen_addresses = '*'/" "${PG_CONF}"
if ! sudo grep -qE "host\s+all\s+all\s+${ALLOW_CIDR//\//\/}\s+md5" "${PG_HBA}"; then
    echo "host    all             all             ${ALLOW_CIDR}            md5" | sudo tee -a "${PG_HBA}" > /dev/null
fi
sudo systemctl restart postgresql

echo 'Provisioning PostgreSQL role and database'
sudo -u postgres psql -v ON_ERROR_STOP=1 <<SQL
DO \$\$
