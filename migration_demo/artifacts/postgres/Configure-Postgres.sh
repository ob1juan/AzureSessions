SELECT p.product_id, p.name, p.product_number, p.color, p.size, p.weight, p.standard_cost, p.list_price,
       c.name AS category_name, pm.name AS product_model, pd.description
FROM saleslt.product p
LEFT JOIN saleslt.product_category c ON c.product_category_id = p.product_category_id
LEFT JOIN saleslt.product_model pm ON pm.product_model_id = p.product_model_id
LEFT JOIN LATERAL (
    SELECT d.description
    FROM saleslt.product_model_product_description x
    INNER JOIN saleslt.product_description d ON d.product_description_id = x.product_description_id
    WHERE x.product_model_id = p.product_model_id
    ORDER BY CASE WHEN x.culture = 'en' THEN 0 ELSE 1 END, x.culture
    LIMIT 1
) pd ON true;

